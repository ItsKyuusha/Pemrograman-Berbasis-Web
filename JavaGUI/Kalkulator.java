    private void Operasi(String operator) {
        try {
            double num1 = Double.parseDouble(angka1.getText());
            double num2 = Double.parseDouble(angka2.getText());
            double result = 0;

            switch (operator) {
                case "+":
                    result = num1 + num2;
                    break;
                case "-":
                    result = num1 - num2;
                    break;
                case "*":
                    result = num1 * num2;
                    break;
                case "/":
                    if (num2 != 0) {
                        result = num1 / num2;
                    } else {
                        JOptionPane.showMessageDialog(this, "Error: Division by zero");
                        return;
                    }
                    break;
                case "%":
                    result = num1 % num2;
                    break;
            }

            // Format hasil untuk menampilkan bilangan bulat jika memungkinkan
            DecimalFormat df = new DecimalFormat("#.##");
            String formattedResult = (result == Math.floor(result)) ? String.valueOf((int) result) : df.format(result);
            hasil.setText(formattedResult);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Invalid input");
        }
    }
    
